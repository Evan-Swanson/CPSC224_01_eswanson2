public class Pet
{
    private String name;
    private String animal;
    private int age;

    public void setName(String n)
    {
        name = n;
    }

    public void setAnimal(String n)
    {
        animal = n;
    }

    public void setAge(int n)
    {
        age = n;
    }

    public String getName()
    {
        return name;
    }

    public String getAnimal()
    {
        return animal;
    }

    public int getAge()
    {
        return age;
    }
}